#include "../includes/fdf.h"

int		ft_parse_fdf(char *line)
{
	int i;

	i = 0;
	while (line[i])
	{
		if ((line[i] >= '0' && line[i] <= '9') || (line[i] == '-') ||
			(line[i] == ' '))
			i++;
		else
			return (-1);
	}
	return (0);
}